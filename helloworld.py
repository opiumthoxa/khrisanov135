f_num = int(input('Введите первое число:'))
oper = input("Введите операцию ")
s_num = int(input("Введите второе число"))
if oper == "+":
    print(f_num + s_num)
elif oper == "-":
    print(f_num + s_num)
elif oper == "*":
    print(f_num + s_num)
elif oper == "/":
    print(f_num + s_num)
else:
    print("ошибка")